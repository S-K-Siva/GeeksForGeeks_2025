class Solution {
  public:
    int getSingle(vector<int> &arr) {
        unordered_map<int,int>mpp;
        for(auto it : arr){
            ++mpp[it];
        }
        for(auto it : mpp){
            if(it.second == 1) return it.first;
        }
        return -1;
    }
};


